#include "libft.h"

int	ft_nbrlen(long nbr)
{
	int	i;

	if (nbr == 0)
		return (1);
	if (nbr < 0)
		nbr *= -1;
	i = 0;
	while (nbr > 0)
	{
		nbr /= 10;
		i++;
	}
	return (i);
}
